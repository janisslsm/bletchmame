/***************************************************************************

    task.cpp

    Abstract base class for asynchronous tasks

***************************************************************************/

#include <QEvent>

#include "task.h"


//**************************************************************************
//  IMPLEMENTATION
//**************************************************************************

//-------------------------------------------------
//  ctor
//-------------------------------------------------

Task::Task()
	: m_hasAborted(false)
{
}


//-------------------------------------------------
//  dtor
//-------------------------------------------------

Task::~Task()
{
}


//-------------------------------------------------
//  start
//-------------------------------------------------

void Task::start(Preferences &prefs)
{
	// by default, do nothing
}


//-------------------------------------------------
//  abort
//-------------------------------------------------

void Task::abort()
{
	// mark as having been aborted
	m_hasAborted = true;
}


//-------------------------------------------------
//  abort
//-------------------------------------------------

bool Task::hasAborted() const
{
	return m_hasAborted;
}
